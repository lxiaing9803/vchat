<template>
  <div
    class="message-input w-full shadow-sm border rounded-lg border-gray-300 py-1 px-2 focus-within:border-green-700"
  >
    <div v-if="imagePreview" class="mb-2 relative flex items-center">
      <img
        :src="imagePreview"
        alt="Preview"
        class="w16 h-16 rounded object-cover"
      />
    </div>
    <div class="flex items-center">
      <input
        type="file"
        accept="image/*"
        class="hidden"
        ref="fileInput"
        @change="handleImageUpload"
      />
      <Icon
        icon="radix-icons:image"
        width="24"
        height="24"
        :class="[
          'mr-2',
          disabled
            ? 'text-gray-300 cursor-not-allowed'
            : 'text-gray-400 cursor-pointer hover:text-gray-600',
        ]"
        @click="triggerFileInput"
      />
      <input
        :disabled="disabled"
        class="outline-none border-0 flex-1 bg-white focus:ring-0"
        type="text"
        v-model="model"
        placeholder="请输入问题"
      />
      <Button
        :disabled="disabled"
        @click="onCreate"
        iconName="radix-icons:paper-plane"
      >
        {{ t("common.send") }}
      </Button>
    </div>
  </div>
</template>

<script lang="ts" setup>
import { ref } from "vue"
import { Icon } from "@iconify/vue"
import Button from "./Button.vue"
import { useI18n } from "vue-i18n"
const props = defineProps<{
  disabled?: boolean
}>()
const emit = defineEmits<{
  create: [value: string, imagePath?: string]
}>()

const { t } = useI18n()

const model = defineModel<string>()
const imagePreview = ref<string>("")
const fileInput = ref<HTMLInputElement | null>(null)
let selectedImage: File | null = null
const triggerFileInput = () => {
  if (!props.disabled) {
    fileInput.value?.click()
  }
}
const handleImageUpload = (event: Event) => {
  const target = event.target as HTMLInputElement
  if (target.files && target.files.length > 0) {
    selectedImage = target.files[0]
    const reader = new FileReader()
    reader.onload = (e) => {
      imagePreview.value = e.target?.result as string
    }
    reader.readAsDataURL(selectedImage)
  }
}

const onCreate = () => {
  if (model.value && model.value.trim() !== "") {
    if (selectedImage) {
      const filePath = window.electronAPI.getFilePath(selectedImage)
      emit("create", model.value, filePath)
    } else {
      emit("create", model.value)
    }
    imagePreview.value = ""
    selectedImage = null
  }
}
</script>
